def get_recommendations(r_val: float, scores: dict, profile: ConflictProfile):
    """
    Генерирует список тактических советов на основе R и конкретных угроз.
    """
    recs = []
    
    # --- 1. СТРАТЕГИЧЕСКИЙ УРОВЕНЬ (По цвету светофора) ---
    # Пороги берем из профиля (обычно 15 и 7)
    red_limit = getattr(profile.thresholds, 'red_limit', 15)
    yellow_limit = getattr(profile.thresholds, 'yellow_limit', 7)

    if r_val >= red_limit:
        recs.append("🔴 **DEFCON 2 (КРАСНЫЙ):** Критическая угроза.")
        recs.append("🚀 Активировать протоколы эвакуации и рассредоточения складов.")
    elif r_val >= yellow_limit:
        recs.append("🟡 **ВНИМАНИЕ (ЖЕЛТЫЙ):** Повышенная вероятность эскалации.")
        recs.append("👁️ Усилить смены дежурных OSINT-аналитиков (режим 24/7).")
    else:
        recs.append("🟢 **ШТАТНЫЙ РЕЖИМ:** Прямых угроз не зафиксировано.")
        recs.append("🛠️ Рекомендуется провести техобслуживание серверов и VPN.")

    # --- 2. ТАКТИЧЕСКИЙ УРОВЕНЬ (По конкретным индикаторам) ---
    
    # L-1: Враг копит силы
    if scores.get("L-1", 0) >= 5:
        recs.append("⚔️ **Угроза прорыва (L-1):** Спутниковая разведка выявила концентрацию сил. Усилить наблюдение за логистическими узлами.")

    # L-20: Жесткие обстрелы
    if scores.get("L-20", 0) >= 5:
        recs.append("💥 **Эскалация (L-20):** Высокий риск ударов по инфраструктуре. Проверить автономные источники питания.")

    # I-7: Информационная атака
    if scores.get("I-7", 0) >= 5:
        recs.append("📢 **ИПСО (I-7):** Фиксируется вброс дезинформации. Подготовить опровержения (факчекинг).")
        
    # I-1: Дыры в ПВО
    if scores.get("I-1", 0) >= 6:
        recs.append("✈️ **Небо (I-1):** Критическая уязвимость ПВО. Рекомендовать смену позиций радаров.")

    # I-8: Дроны
    if scores.get("I-8", 0) >= 5:
        recs.append("🤖 **Дроны (I-8):** Враг применяет новые технологии. Активировать РЭБ-протоколы.")

    # Если всё совсем тихо и специфических угроз нет
    if len(recs) <= 2:
        recs.append("✅ Дополнительных специфических угроз не выявлено.")

    return recs